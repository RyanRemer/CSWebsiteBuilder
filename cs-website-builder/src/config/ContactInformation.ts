export class ContactInformation {
    name: String;
    email: String;
    phoneNumber: String;
    officeLocation: String;


    constructor(name: String, email: String, phoneNumber: String, officeLocation: String,) {
        this.name = name;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.officeLocation = officeLocation
    }

}